            vendedor.setAcceso(txtAcceso.getText());
            vendedor.setUsuario(txtUsuario.getText());
            vendedor.setPassword(txtPassword.getText());
            vendedor.setSexo(cmbGenero.getSelectedIndex() == 1 ? "m" : "f");
